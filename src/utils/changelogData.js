// Найти changelog для всех версий между prevVersion и currentVersion (включительно).
// Возвращает массив entries для отображения в модалке.
export function getChangelogSince(prevVersion, currentVersion) {
  if (!currentVersion) return []
  // Если первая установка (нет prevVersion) — показываем только последнюю.
  if (!prevVersion) {
    return CHANGELOG.slice(0, 1)
  }
  if (prevVersion === currentVersion) return []
  const result = []
  for (const entry of CHANGELOG) {
    if (compareVersions(entry.version, prevVersion) > 0
      && compareVersions(entry.version, currentVersion) <= 0) {
      result.push(entry)
    }
  }
  return result
}

// Сравнивает 'X.Y.Z' формат: -1 если a<b, 0 если =, +1 если a>b.
function compareVersions(a, b) {
  const pa = String(a).split('.').map(Number)
  const pb = String(b).split('.').map(Number)
  const len = Math.max(pa.length, pb.length)
  for (let i = 0; i < len; i++) {
    const av = pa[i] || 0
    const bv = pb[i] || 0
    if (av < bv) return -1
    if (av > bv) return 1
  }
  return 0
}
